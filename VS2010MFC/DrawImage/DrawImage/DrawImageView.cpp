void CDrawImageView::OnDraw(CDC* pDC)
{
	CDrawImageDoc* pDoc = GetDocument();
	ASSERT_VALID(pDoc);
	if (!pDoc)
		return;

	// TODO: 在此处为本机数据添加绘制代码
	if(!src.empty())
	{
		CRect rc;
		GetClientRect(&rc);  //src = imread("C:\\Users\\asus\\Desktop\\14296978024588.png");
		SetRect(rc, 0, 0, src.cols, src.rows);  
		IplImage img = src;
		CvvImage cimg;
		cimg.CopyOf(&img);
		cimg.DrawToHDC(pDC->GetSafeHdc(), rc);
	}
}


